 *
 * @author Augusto Gava
 * @version 1.0
 */
class PropriedadesFornecedores  {
	var $id;
	var $nome;
	var $razao;
	
	public function getId(){
		return $this->id;
	}
	
	public function setId($id){
		$this->id = $id;
	}
	
	public function getNome(){
		return $this->nome;
	}
	
	public function setNome($nome){
		$this->nome = $nome;
	}
	
	public function getRazao(){
		return $this->razao;
	}
	
	public function setRazao($razao){
		$this->razao = $razao;
	}
	
}
